public class Revision {
    public static void main(String args[]){
        int n = 10899;
        int lastdigit;
        int removelast;
        while(n>0){
            lastdigit = n%10;
            system.out.println(lastdigit);
            removelast = n/10;
            system.out.println(removelast);
        }
    }
   
}

